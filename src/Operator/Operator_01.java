package Operator;

public class Operator_01 {
    public static void main(String[] args) {
        int x = -100;
        int result1 = +x;
        int result2 = -x;
        System.out.println("result 1 : " + result1);
        System.out.println("result 2 : " + result2);

        short s = 100;
        int result3 = -s;
        System.out.println("result3 : " + result3);
    }
}
